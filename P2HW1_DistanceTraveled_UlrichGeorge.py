#A car is traveling at 70 miles per hour. Display the following:
#The distance the car will travel in 6 hours.
#The distance the car will travel in 10 hours.
#The distance the car will travel in 15 hours.

print("After 6 hours:")
SPEED = 70
time_1 = 6
distanceAfter6 = SPEED * time_1

print("The distance traveled in 6 hours is" , (distanceAfter6))
#time_1/2/3 is an easy way of differentiating them and keeping them in order.
print("After 10 hours:")
SPEED = 70
time_2 = 10
distanceAfter10 = SPEED * time_2

print("The distance traveled in 10 hours is" , (distanceAfter10))
#Using SPEED more than once is not necessary as it is a constant.
#However using SPEED this way helps comprehension.
print("After 15 hours:")
SPEED = 70
time_3 = 15
distanceAfter15 = SPEED * time_3

print("The distance traveled in 15 hours is" , (distanceAfter15))
